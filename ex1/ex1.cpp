int isUpperCaseWord(char arr[], int start, int end) {
    for (int i = start; i <= end; i++) {
        if (!isupper(arr[i])) {
            return 0;
        }
    }
    return 1;
}

void replaceWithXO(char arr[], int start, int end) {
    int length = end - start + 1;
    for (int i = start; i <= end; i++) {
        if ((i - start) % 2 == 0) {
            arr[i] = 'X';
        }
        else {
            arr[i] = 'O';
        }
    }
}

void processStringArray(char arr[], int length) {
    int start = -1;

    for (int i = 0; i <= length; i++) {
        if (i < length && isalpha(arr[i])) {
            if (start == -1) {
                start = i;
            }
        }
        else {
            if (start != -1) {
                int end = i - 1;
                if (isUpperCaseWord(arr, start, end)) {
                    replaceWithXO(arr, start, end);
                }
                start = -1;
            }
        }
    }
}

